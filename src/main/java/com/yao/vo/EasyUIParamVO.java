package com.yao.vo;

public class EasyUIParamVO {

	//要排序的参数名
	private String sort;
	//排序的方式 升序：asc ,降序：desc
	private String order;
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	public String getOrder() {
		return order;
	}
	public void setOrder(String order) {
		this.order = order;
	}
	
}
